package cn.entity;

public class Grade {
	private int gradeId;
	private String gradeName;
	
	public void setGradeId(int gradeId){
		this.gradeId = gradeId;
	}
	public int getGradeId(){
		return gradeId;	
	}
	
	
	public void setGradeName(String gradeName){
		this.gradeName = gradeName;
	}
	public String getGradeName(){
		return gradeName;
	}
	
}
